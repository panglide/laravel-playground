    {
        $teachers = Profile::select('t.id', 'fname', 'lname')
            ->leftjoin('teachers as t', 't.id', 'teacher_id')
            ->where('school_id', $request->school_id)
            ->get();

        return response(['teachers' => $teachers], 200);
